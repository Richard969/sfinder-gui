export const translations: Record<Lang, typeof en> = { en, zh };

// Flattened key access: t('sidebar.fumenEditor') → "Fumen Editor"
type NestedKeys<T, K extends string = ''> = T extends object
  ? { [P in keyof T]: NestedKeys<T[P], `${K}${string & P}${'' extends K ? '' : '.'}`> }[keyof T]
  : K;

export function t(lang: Lang, key: string): string {
  const dict = translations[lang] as Record<string, unknown>;
  const parts = key.split('.');
  let current: unknown = dict;
  for (const part of parts) {
    if (current && typeof current === 'object') {
      current = (current as Record<string, unknown>)[part];
    } else {
      return key; // fallback: show key itself
    }
  }
  return typeof current === 'string' ? current : key;
}

export function useT(lang: Lang) {
  return (key: string) => t(lang, key);
}
